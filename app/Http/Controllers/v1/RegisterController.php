    {
      $employee = Employee::where('emp_num', $request->input('emp_num'))->firstOrFail();

        $employee->account()->create([
          'emp_num' => $request->input('emp_num'),
          'password' => bcrypt($request->input('password'))
        ]);

        return response()->json($employee->account);
    }

